#include <stdio.h>

int main()
{
    //    \n in multiple lines of codes
    printf("Hello!\n");
    printf("This is me\n\n");

    //    \n in one line of codes 
    printf("Hello!\nThis is me\n\n");

    //    \t Creates a horizontal tab
    printf("Hello!\t This is me\n\n");

    //    \\ Inserts a backslash character (\)
    printf("Hello!\\ \n\n");

    //    \" Inserts a double quote character
    printf("Hello! I'm \"Jocelyn\"\n\n");
    
    return 0;
}